window.addEventListener("keydown", function (event) {
  switch (event.code) {
    case "KeyW":
      break;
    case "KeyS":
      break;
    case "KeyA":
      break;
    case "KeyD":
      break;
    case "ArrowUp":
      break;
    case "ArrowDown":
      break;
    case "ArrowLeft":
      break;
    case "ArrowRight":
      break;
  }

}, false);

